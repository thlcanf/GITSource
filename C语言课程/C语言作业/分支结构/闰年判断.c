// 试题描述
//    通常说：“四年一闰，百年不闰，四百年再闰”。请你判断一个年份是否为闰年？
// 输入
//    输入一个正整数n，且0 < n <= 30000。
// 输出
//    如果为闰年则输出“TRUE”，否则输出“FALSE”（不输出引号）。
// 输入示例
//    2000
// 输出示例
//    TRUE
// 数据范围
//    对于100%的数据，0 < n <= 30000
#include<stdio.h>
int main()
{
    int a;
    scanf("%d",&a);
    if((a%4==0&&a%100!=0)||(a%400==0))
    {
        printf("TRUE");
    }
    else
    {
        printf("FALSE");
    }
    return 0;
}
